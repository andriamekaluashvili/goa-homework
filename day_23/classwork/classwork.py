# ფუნქცია, რომელიც მომხმარებელს ატანინებს რიცხვებს 5-ის ჯერადამდე
def get_multiple_of_five():
    while True:  # ვიმეორებთ სანამ არ მიიღწევა 5-ის ჯერადი რიცხვი
        try:
            number = int(input("შეიყვანეთ რიცხვი: "))  # ვიღებთ რიცხვს
            if number % 5 == 0:  # проверяем, чи є це кратним 5
                print(f"{number} არის 5-ის ჯერადი!")
                break  # გამოდის ციკლიდან
            else:
                print(f"{number} არ არის 5-ის ჯერადი! სცადეთ კიდევ ერთხელ.")
        except ValueError:  # არასწორი რიცხვის შემთხვევაში
            print("გთხოვთ, შეიტანოთ რიცხვი.")

# ამ ფუნქციის გაშვება
get_multiple_of_five()
